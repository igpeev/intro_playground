# range - arithmetic progression of integers

# range(5)
range(0, 5)

for i in range(5):  # <-- note ONE PAST THE STOP VALUE !!!
    print(i)
# 0
# 1
# 2
# 3
# 4

range(5, 10)        # <-- range starting from 5
# range(5, 10)

range(5, 10, 3)     # <-- start, stop, step
list(range(5, 20, 3))   # <-- convenient way to geneerate list (array)
# [5, 8, 11, 14, 17]

